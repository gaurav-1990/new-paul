        <h1 class="content-title">Fabric Care & Wash Guidelines</h1>

<div class="text-block">
           
            <p>Paulsons take pride in offering their clothes and we know that they mean the world to you. All our products are crafted using the superior quality of fabric and fibers, so, they need additional love and care.  All our designs are either hand-picked from the best manufac-turers & weavers or designed in-house products using the best fabrics.</p>
  
  <p>In fact the irregularities, variations and diversity in printing techniques, makes the prod-uct unique and these are the real trademark of its superiority like slub in handloom & khadi silk or outcome of meticulous labour of artisans which proves in the form of irregu-larity in block prints or ajrakh prints. </p>
  <p>Mindfully designing and creating each product is a key task, however, caring for them is even more. With little care and effort, you can extend the life of product and preserve their colour and sheen for longer periods, maybe even years!</p>
   
   <h4>Although, Separate wash instructions are usually prescribed for each fabric, you can also follow the same while washing: </h4>
   
   <ul>
   <li>•	Cotton: Cotton as a fiber is robust yet soft. The more it is washed, more breathable it becomes. However, it is advisable to wash them separately using cold water and mild detergent.  Expect little shrinkage in cottons upon your initial wash.</li>
   <li>•	Silks, Crepes & Georgettes: The sheen and luster of these fabrics is highly graceful. It gets weakest when it gets wet. We highly recommend dry cleaning for best results.</li>
  <li>•	Chanderi & Maheshwari: These are beautiful fabrics with a lustre of its own. Be it handloom or powerloom produced, these are naturally delicate fabrics & are to  be handled with care. We recommend them to be separately hand washed or dry-cleaned if they are embroidered or embellished. </li>
  <li>•	In addition to above, some other precautions while laundering as follows:
  <ul>
  <li>o	Wash dark colors and light colours fabrics separately as dark colours may bleed </li>
  <li>o	We recommend using hand-wash while cleaning at home</li>
  <li>o	To make the garments more attractive and unique, various printing tech-niques, embroideries, or embellishments are done. In that case we recom-mend you to get the garments dry-cleaned.</li>
  <li>o	Avoid soaking or scrubbing hard with brush.</li>
  <li>o	Never wash with bleach or chlorine.</li>
  <li>o	Never dry in under the direct sun.  It can fade the colours away. Always dry in shade</li>
  <li>o	Iron on medium temperature is recommended</li>
  <li>
  o	Each fabric is different, so is their life and few does fades over the time which is not a defect.
  </li>
  <li>o	Don’t soak the fabrics in water for longer duration, it reduces the durability of the fabrics. It can also lead to bleeding of colours.</li>
  </ul>
  
  
  </li>
   </ul>
   
