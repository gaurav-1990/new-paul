<h3>Placing a Cash-on-Delivery (COD) Order </h3>

<p>1. When prompted to choose a Payment Option, select "Pay By Cash-on-Delivery" </p>
<p>2. Our Customer Care team will call you to verify the details.  </p>
<p>3. Once verified and confirmed the order will be processed for shipment in the time specified, from the date of confirmation. </p>
<p>4. You will be required to make a Cash payment (no cheque or DD will be accepted) to our Courier Partner at the time of delivery. </p>
<p>5. For Cash on Delivery orders packages cannot be opened until complete payment has been made to the Firstcry representative. 
</p>


