        <h3>EMI Payment Option</h3>

      <p>EMI stands for 'Equated Monthly Installment' and this option gives you the flexibility to convert your credit card purchases to monthly installments. 
      With EMI you can pay for your order in monthly installments over a period (depending on the tenure and bank selected) instead of paying all at once. For
       Example you purchased a product worth 6000 on 3 months EMI then your monthly installment would be Rs. 2000 each spread across 3 months. </p>

